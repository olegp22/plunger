        // Расстояние (px) при котором считаем "добрались до точки" → отцеп
        public const double DetachDistance = 6.0;

        // Тиков без прогресса → принудительный отцеп (антизависание)
        public const int StuckTicks = 0;

        // Минимальное уменьшение dist за тик чтобы считать "прогресс"
        public const double StuckThreshold = 0.5;

        // Дальность броска присоски (px)
        public const double GrappleRange = 1200.0;

        // Скорость снаряда-присоски (px за тик)
        public const float ProjectileSpeed = 100f;

        // ── ПРИЦЕЛИВАНИЕ ─────────────────────────────────────────────────────
        // Угол поворота прицела за тик (градусов). Больше = быстрее
        public const double AimStep = 3.5;
        public const double AimMin = -90.0;   // вверх
        public const double AimMax = 85.0;   // вниз
    }
}